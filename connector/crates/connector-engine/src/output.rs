//! Output Builder — assembles PipelineOutput from both kernels.
//!
//! Collects results from VAC (memory ops, audit) and AAPI (action records,
//! authorization decisions) into a single, developer-friendly output struct.
//!
//! ## Design Principles (industry-leading observability)
//!
//! 1. **Provenance-tagged**: Every field tagged with source (Kernel/Llm/Derived/User)
//!    — zero possibility of fake/hallucinated observability data
//! 2. **Dual-audience**: Human-readable Display + machine-parseable JSON + OTel export
//! 3. **Kernel-backed**: All metrics come from real kernel audit entries, not estimates
//! 4. **Event stream**: Structured ObservationEvents for every significant action
//! 5. **Warnings/errors**: Auto-collected from denied actions, failed guardrails
//!
//! ## Research basis
//!
//! - OpenTelemetry GenAI Semantic Conventions (v1.36+)
//! - Langfuse trace/span/generation/event model
//! - Arize Phoenix hallucination detection signals
//! - Datadog LLM Observability structured traces
//! - Helicone cost/token tracking per request

