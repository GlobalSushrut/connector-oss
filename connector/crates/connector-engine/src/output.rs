pub struct OutputBuilder;

impl OutputBuilder {
    /// Build a PipelineOutput from the current kernel state.
    ///
    /// All metrics are extracted from real kernel audit entries — zero estimates.
    /// Events and warnings are auto-collected from denied/failed/pending operations.
    pub fn build(
        kernel: &MemoryKernel,
        text: String,
        pipeline_id: &str,
        actor_count: usize,
        compliance: &[String],
        duration_ms: u64,
        memories: Vec<ConnectorMemory>,
    ) -> PipelineOutput {
        Self::build_with_aapi(kernel, text, pipeline_id, actor_count, compliance, duration_ms, memories, None)
    }

    /// Build with real AAPI ActionEngine stats.
    pub fn build_with_aapi(
        kernel: &MemoryKernel,
        text: String,
        pipeline_id: &str,
        actor_count: usize,
        compliance: &[String],
        duration_ms: u64,
        memories: Vec<ConnectorMemory>,
        aapi_stats: Option<ActionEngineStats>,
    ) -> PipelineOutput {
        let trust = TrustComputer::compute(kernel);
        let audit_log = kernel.audit_log();

        let authorized = audit_log.iter()
            .filter(|e| e.outcome == OpOutcome::Success)
            .count();
        let denied = audit_log.iter()
            .filter(|e| e.outcome == OpOutcome::Denied)
            .count();
        let pending = audit_log.iter()
            .filter(|e| e.outcome == OpOutcome::Skipped)
            .count();
        let failed = audit_log.iter()
            .filter(|e| e.outcome == OpOutcome::Failed)
            .count();
        let vakya_count = audit_log.iter()
            .filter(|e| e.vakya_id.is_some())
            .count();

        let mem_writes = audit_log.iter()
            .filter(|e| e.operation == vac_core::types::MemoryKernelOp::MemWrite && e.outcome == OpOutcome::Success)
            .count();
        let mem_reads = audit_log.iter()
            .filter(|e| e.operation == vac_core::types::MemoryKernelOp::MemRead && e.outcome == OpOutcome::Success)
            .count();
        let access_grants = audit_log.iter()
            .filter(|e| e.operation == vac_core::types::MemoryKernelOp::AccessGrant && e.outcome == OpOutcome::Success)
            .count();

        let summary = format!(
            "{} actors, {} steps, {} memories, trust: {}/100 ({}), {}",
            actor_count,
            audit_log.len(),
            mem_writes,
            trust.score,
            trust.grade,
            if compliance.is_empty() { "no compliance".to_string() }
            else { compliance.join(", ") + " ✓" }
        );

        // ── Build observation events from kernel audit log ───────
        let mut events = Vec::new();
        let mut warnings = Vec::new();
        let mut errors = Vec::new();

        for entry in audit_log.iter() {
            let human_label = crate::trace::op_to_human(&entry.operation);
            let event_type = crate::trace::op_to_llm(&entry.operation);

            let (severity, message) = match &entry.outcome {
                OpOutcome::Success => (
                    EventSeverity::Info,
                    format!("{} by {}", human_label, short_pid(&entry.agent_pid)),
                ),
                OpOutcome::Denied => {
                    let msg = format!("{} DENIED for {} — {}", human_label, short_pid(&entry.agent_pid),
                        entry.error.as_deref().unwrap_or("authorization failed"));
                    warnings.push(msg.clone());
                    (EventSeverity::Warning, msg)
                },
                OpOutcome::Failed => {
                    let msg = format!("{} FAILED for {} — {}", human_label, short_pid(&entry.agent_pid),
                        entry.error.as_deref().unwrap_or("operation error"));
                    errors.push(msg.clone());
                    (EventSeverity::Error, msg)
                },
                OpOutcome::Skipped => {
                    let msg = format!("{} requires approval for {}", human_label, short_pid(&entry.agent_pid));
                    warnings.push(msg.clone());
                    (EventSeverity::Warning, msg)
                },
                OpOutcome::Pending => {
                    let msg = format!("{} pending for {}", human_label, short_pid(&entry.agent_pid));
                    (EventSeverity::Warning, msg)
                },
            };

            events.push(ObservationEvent {
                event_type: event_type.to_string(),
                severity,
                message,
                agent: Some(entry.agent_pid.clone()),
                cid: entry.target.clone(),
                source: Provenance::Kernel,
                timestamp_ms: entry.timestamp,
            });
        }

        // Add trust warning if low
        if trust.score < 50 {
            warnings.push(format!("Low trust score: {}/100 — review kernel integrity", trust.score));
        }

        // ── Build structured trace ───────────────────────────────
        let actor_names: Vec<String> = (0..actor_count).map(|i| format!("actor_{}", i)).collect();
        let trace = TraceBuilder::from_kernel(
            kernel,
            pipeline_id.split(':').nth(1).unwrap_or(pipeline_id),
            pipeline_id,
            &actor_names,
            compliance,
            duration_ms,
            trust.score,
            &trust.grade,
            &trust.dimensions,
        );

        PipelineOutput {
            text,
            status: PipelineStatus {
                ok: denied == 0 && failed == 0,
                trust: trust.score,
                trust_grade: trust.grade.clone(),
                actors: actor_count,
                steps: audit_log.len(),
                duration_ms,
                total_tokens: 0,
                total_cost_usd: 0.0,
                summary,
            },
            aapi: AapiSummary {
                authorized,
                denied,
                pending_approval: pending,
                vakya_count,
                compliance: compliance.to_vec(),
                action_records: aapi_stats.as_ref().map(|s| s.action_records).unwrap_or(0),
                interaction_count: aapi_stats.as_ref().map(|s| s.interaction_count).unwrap_or(0),
                policy_count: aapi_stats.as_ref().map(|s| s.policy_count).unwrap_or(0),
                capability_count: aapi_stats.as_ref().map(|s| s.capability_count).unwrap_or(0),
                budget_count: aapi_stats.as_ref().map(|s| s.budget_count).unwrap_or(0),
            },
            memory: MemorySummary {
                created: mem_writes,
                recalled: mem_reads,
                shared: access_grants,
                total_packets: kernel.packet_count(),
                memories,
            },
            connector: ConnectorSummary {
                control_plane: "active".to_string(),
                pipeline_id: pipeline_id.to_string(),
                audit_entries: audit_log.len(),
                trust_details: trust,
            },
            trace,
            events,
            warnings,
            errors,
            run_trace: None,
        }
    }
}

