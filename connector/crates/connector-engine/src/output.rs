/// Observation event severity.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "snake_case")]
pub enum EventSeverity {
    Info,
    Warning,
    Error,
}

/// A structured observation event — one significant thing that happened.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ObservationEvent {
    /// Event type (e.g., "memory.write", "tool.denied", "approval.required")
    pub event_type: String,
    /// Severity level
    pub severity: EventSeverity,
    /// Human-readable message
    pub message: String,
    /// Agent that triggered this event
    pub agent: Option<String>,
    /// Related CID (evidence)
    pub cid: Option<String>,
    /// Provenance source
    pub source: Provenance,
    /// Timestamp (ms since epoch)
    pub timestamp_ms: i64,
}

