/// Provenance source — where did this data come from?
///
/// This is the zero-fake guarantee: every observation field is tagged
/// so developers and auditors know exactly what is kernel-verified
/// vs what came from an LLM (unverified).
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "snake_case")]
pub enum Provenance {
    /// Verified by kernel — CID-backed, Merkle-verified, tamper-proof.
    /// Trust this completely.
    Kernel,
    /// Generated by LLM — unverified, may hallucinate.
    /// Treat as advisory only.
    Llm,
    /// Computed/derived from kernel data — deterministic calculation.
    /// Trust if kernel inputs are trusted.
    Derived,
    /// Provided by user/developer — external input.
    User,
}

impl std::fmt::Display for Provenance {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Kernel => write!(f, "kernel"),
            Self::Llm => write!(f, "llm"),
            Self::Derived => write!(f, "derived"),
            Self::User => write!(f, "user"),
        }
    }
}

/// A provenance-tagged value — wraps any value with its source + optional CID evidence.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Verified<T: Clone> {
    /// The actual value
    pub value: T,
    /// Where this value came from
    pub source: Provenance,
    /// CID evidence (if kernel-sourced, this is the content-address proof)
    pub evidence_cid: Option<String>,
}

impl<T: Clone> Verified<T> {
    pub fn kernel(value: T) -> Self {
        Self { value, source: Provenance::Kernel, evidence_cid: None }
    }
    pub fn kernel_with_cid(value: T, cid: &str) -> Self {
        Self { value, source: Provenance::Kernel, evidence_cid: Some(cid.to_string()) }
    }
    pub fn llm(value: T) -> Self {
        Self { value, source: Provenance::Llm, evidence_cid: None }
    }
    pub fn derived(value: T) -> Self {
        Self { value, source: Provenance::Derived, evidence_cid: None }
    }
    pub fn user(value: T) -> Self {
        Self { value, source: Provenance::User, evidence_cid: None }
    }
    pub fn is_trusted(&self) -> bool {
        matches!(self.source, Provenance::Kernel | Provenance::Derived)
    }
}

